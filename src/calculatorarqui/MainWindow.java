    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if ((String) comboBoxCurrency.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(rootPane, "No hay archivos para eliminar!", "Datos", WIDTH);
            return;
        }
        if (JOptionPane.showConfirmDialog(rootPane, "¿Desea realmente eliminar la moneda?",
                "Eliminar moneda", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {

            quemarDatos(null, null, null, (String) comboBoxCurrency.getSelectedItem(), "");
            eliminarArchivo((String) comboBoxCurrency.getSelectedItem());

            if (monedas.size() == 0) {
                CrearEscribirArchivo("Moneda", null);
            }
            //Escribo en el archivo la nueva moneda
            for (String moneda : monedas) {

                CrearEscribirArchivo("Moneda", moneda);

            }
            JOptionPane.showMessageDialog(rootPane, "La moneda fue eliminada!", "Eliminando moneda", WIDTH);
            refrescarVentana();

        }

    }//GEN-LAST:event_btnEliminarActionPerformed

