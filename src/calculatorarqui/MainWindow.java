    public MainWindow() {
        initComponents();
        this.setLocationRelativeTo(null);

        //Leo el archivo moneda para imprimir en el comboBox las monedas que tengo
        File archivo;
        FileReader leer;
        BufferedReader almacenamiento;
        String cadena;
        archivo = new File("Moneda.txt");

        if (!archivo.exists()) {
            CrearEscribirArchivo("Moneda", null);
        }

        try {

            leer = new FileReader(archivo);
            almacenamiento = new BufferedReader(leer);
            cadena = "";
            while (cadena != null) {

                try {
                    cadena = almacenamiento.readLine();
                    if (cadena == null) {
                        almacenamiento.close();
                        leer.close();
                        return;
                    }
                    comboBoxCurrency.addItem(cadena);
                } catch (IOException ex) {
                    Logger.getLogger(MainWindow.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            try {
                almacenamiento.close();
                leer.close();

            } catch (IOException ex) {
                Logger.getLogger(MainWindow.class.getName()).log(Level.SEVERE, null, ex);
            }
        } catch (FileNotFoundException ex) {
            Logger.getLogger(MainWindow.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    public void eliminarArchivo(String name) {
        File archivo;
        StringBuffer n = new StringBuffer();
        n.append(name);
        n.append(".txt");
        archivo = new File(n.toString());
        archivo.delete();
    }

    public void refrescarVentana() {
        MainWindow n = new MainWindow();
        n.show();
        this.dispose();
    }

